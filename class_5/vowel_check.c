/*
 * vowel_check.c
 * ユーザーが入力した1文字（char）を判定して、母音か子音かを表示するサンプル
 * 制約: main 関数のみを使用（他の関数は作らない）
 *
 * 使い方（実行時に1文字を入力）:
 *   $ ./vowel_check
 *   文字を1つ入力してください: a
 *   入力: 'a' -> 母音です
 *
 * 備考:
 *  - 小文字の英字のみ判定（'a','i','u','e','o' を母音とする）
 *  - サンプルの簡潔さを優先しており、大文字や非英字の扱いは教示コメントで補足
 */

#include <stdio.h>

int main(void) {
    char ch;

    printf("文字を1つ入力してください: ");
    /* 前の空白文字をスキップするために書式指定子の前に空白を入れる */
    if (scanf(" %c", &ch) != 1) {
        printf("入力エラー: 1 文字を入力してください\n");
        return 1;
    }

    /* 判定: 小文字の母音かどうかを switch で確認する */
    switch (ch) {
        case 'a':
        case 'i':
        case 'u':
        case 'e':
        case 'o':
            printf("入力: '%c' -> 母音です\n", ch);
            break;
        default:
            printf("入力: '%c' -> 子音です\n", ch);
            break;
    }

    /* 教育的補足（コメントで説明）:
     * - 小文字のみチェックしているので、大文字も扱いたい場合は
     *   'A','I','U','E','O' を追加するか、tolower() を使う。
     * - 英字以外（数字や記号）を弾く場合は isalpha() などを使って事前に検査する。
     */

    return 0;
}
